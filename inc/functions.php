//return current active networks
function ultimatesocialshare_active_networks() {
	$ultimatesocialshare = get_option('ultimatesocialshare')['general'];
	$networks  = array();
	foreach($ultimatesocialshare as $location) {
		if(!empty($location['enabled']) && $location['enabled'] == "1" && !empty($location['social_networks'])) {
			foreach($location['social_networks'] as $key => $network) {
				if(!in_array($network, $networks)) {
					$networks[] = $network;
				}
			}
		}
	}
	return $networks;
}

