//return network share link
function ultimatesocialshare_network_link($network) {

	if(empty($network)) {
		return '';
	}

	global $wpdb;
	global $post;

	//make sure we have valid post
	if(!$post || !in_array($post->post_status, array('publish', 'private'))) {
		return '';
	}

	$ultimatesocialshare = get_option('ultimatesocialshare')['general'];

	//post permalink
	$permalink = get_the_permalink($post->ID);

	//add Google UTM tracking
	if(!empty($ultimatesocialshare['google_utm']) && !empty($ultimatesocialshare['google_utm_source']) && !empty($ultimatesocialshare['google_utm_medium']) && !empty($ultimatesocialshare['google_utm_name'])) {
		$google_utm = array(
			'utm_source' => (trim($ultimatesocialshare['google_utm_source']) === '{{network}}' ? $network : trim($ultimatesocialshare['google_utm_source'])),
			'utm_medium' => trim($ultimatesocialshare['google_utm_medium']),
			'utm_campaign' => trim($ultimatesocialshare['google_utm_name'])
		);
		$permalink = add_query_arg($google_utm, $permalink);
	}

	//bitly link shortening
	if(!empty($ultimatesocialshare['enable_bitly']) && $network != 'pinterest' && $network != 'hackernews') {
		$permalink = ultimatesocialshare_bitly_link($permalink, $network);
	}

	//encode final permalink
	$permalink = rawurlencode($permalink);

	//get existing details row
	$details = ultimatesocialshare_get_post_details($post->ID);

	//title
	$title = !empty($details['social_title']) ? $details['social_title'] : $post->post_title;

	//image
	$social_image = '';

	if(!empty($details['social_image'])) {
		$social_image = wp_get_attachment_url($details['social_image']);
	}
	if(empty($social_image)) {
		$social_image = get_the_post_thumbnail_url($post->ID, 'full');
	}

	//apply filters
	$title = rawurlencode(apply_filters('ultimatesocialshare_meta_title', $title, $network));

	//build link for network
	switch($network) {
		case 'twitter':
			$link = "https://twitter.com/intent/tweet?text=" . $title . "&url=" . $permalink . (!empty($ultimatesocialshare['twitter_username']) ? "&via=" . $ultimatesocialshare['twitter_username'] : "");
			break;

		case 'facebook':
			$link = "https://www.facebook.com/sharer/sharer.php?u=" . $permalink;
			break;

		case 'linkedin':
			$link = "https://www.linkedin.com/sharing/share-offsite/?url=" . $permalink;
			break;

		case 'pinterest':
			$pinterest_image = '';
			if(!empty($details['pinterest_image'])) {
				$pinterest_image = wp_get_attachment_url($details['pinterest_image']);
			}
			if(empty($pinterest_image)) {
				$pinterest_image = $social_image;
			}
			$pinterest_title = !empty($details['pinterest_title']) ? rawurlencode(apply_filters('ultimatesocialshare_meta_title', $details['pinterest_title'], 'pinterest')) : $title;
			if($pinterest_image) {
				$link = "http://pinterest.com/pin/create/button/?url=" . $permalink . "&media=" . $pinterest_image . "&description=" . $pinterest_title;
			}
			else {
				$link = '#';
			}
			break;

		case 'buffer':
			$link = "https://buffer.com/add?url=" . $permalink . "&text=" . $title;
			break;

		case 'reddit':
			$link = "https://www.reddit.com/submit?url=" . $permalink . "&title=" . $title;
			break;

		case 'hackernews':
			$link = "https://news.ycombinator.com/submitlink?u=" . $permalink . "&t=" . $title;
			break;


		case 'pocket':	
			$link = "https://getpocket.com/edit?url=" . $permalink;
			break;

		case 'whatsapp':
			$link = "https://api.whatsapp.com/send?text=" . $title . "+" . $permalink;
			break;

		case 'tumblr':
			$link = "https://www.tumblr.com/widgets/share/tool?canonicalUrl=" . $permalink;

		case 'vkontakte':
			$link = "http://vk.com/share.php?url=" . $permalink;
			break;

		case 'xing':
			$link = "https://www.xing.com/spi/shares/new?url=" . $permalink;
			break;

		case 'flipboard':
			$link = "https://share.flipboard.com/bookmarklet/popout?v=2&url=" . $permalink . "&title=" . $title;
			break;

		case 'telegram':
			$link = "https://telegram.me/share/url?url=" . $permalink . "&text=" . $title;
			break;

		case 'mix':
			$link = "https://mix.com/add?url=" . $permalink;
			break;

		case 'yummly':
			$link = "https://www.yummly.com/urb/verify?url=" . $permalink . "&title=" . $title . "&image=" . $social_image . "&yumtype=button";
			break;

		case 'sms':
			$link = "sms:?&body=" . $title . "%20" . $permalink;
			break;

		case 'messenger':
			$link = "fb-messenger://share/?link=" . $permalink;
			break;

		case 'line':
			$link = "https://lineit.line.me/share/ui?url=" . $permalink . "&text=" . $title;
			break;

		case 'email':
			$link = "mailto:?subject=" . esc_attr($title) . "&amp;body=" . $permalink;
			break;

		case 'print':
			$link = '#';
			break;

		default:
			$link = '';
			break;
	}

	//return final link
	return $link;
}

//display inline content
function ultimatesocialshare_inline_content($content, $direct = false) {

	global $wp_current_filter;

	//bail if the_content is being requested by something else
    if(!empty($wp_current_filter) && is_array($wp_current_filter)) {
    	if(count(array_intersect($wp_current_filter, array('wp_head', 'get_the_excerpt', 'widget_text_content', 'p3_content_end'))) > 0) {
	     	return $content;
		}
    }

	if(!is_singular()) {
		return $content;
	}

	$ultimatesocialshare = get_option('ultimatesocialshare')['general']; 

	if(!empty($ultimatesocialshare['inline']['position']) && $ultimatesocialshare['inline']['position'] == 'neither' && $direct == false) {
		return $content;
	}

