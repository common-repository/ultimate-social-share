									$output.= "body .ultimatesocialshare-inline:not(.ultimatesocialshare-columns) a.ultimatesocialshare-button, body .ultimatesocialshare-inline .ultimatesocialshare-total-share-count {
										margin: 0px 5px 10px 5px;
									}";
								}
							}
						}
						if(!empty($ultimatesocialshare['inline']['total_share_count'])) {
							if(!empty($ultimatesocialshare['inline']['total_share_count_color'])) {
