			}
			elseif($ultimatesocialshare[$location]['style'] == 'full-inverse-border') {
				$button_class = " ultimatesocialshare-inverse ultimatesocialshare-border" . $shape_class;
				$icon_class.= " ultimatesocialshare-inverse";
				$label_class.= " ultimatesocialshare-inverse";
			}
			elseif($ultimatesocialshare[$location]['style'] == 'solid-inverse') {
				$button_class = " ultimatesocialshare-inverse";
				$icon_class.= $shape_class;
				$label_class.= " ultimatesocialshare-inverse";
			}
			elseif($ultimatesocialshare[$location]['style'] == 'full-inverse') {
				$button_class = " ultimatesocialshare-inverse";
				$icon_class.= " ultimatesocialshare-inverse";
				$label_class.= " ultimatesocialshare-inverse";
			}
		}
		else {
			$button_class = $shape_class;
		}

		//layout container classes
		if(!empty($ultimatesocialshare[$location]['layout'])) {
			$container_class.= " ultimatesocialshare-columns ultimatesocialshare-" . $ultimatesocialshare[$location]['layout'];
		}

		//size container class
		if(!empty($ultimatesocialshare[$location]['size'])) {
			$container_class.= " " . $ultimatesocialshare[$location]['size'];
		}

		//share count container classes
		if(!empty($share_counts)) {
			$container_class.= " ultimatesocialshare-share-count";
			if(!empty($ultimatesocialshare[$location]['total_share_count'])) {
				$container_class.= " ultimatesocialshare-has-total-share-count-";
				$container_class.= (!empty($ultimatesocialshare[$location]['total_share_count_position']) ? $ultimatesocialshare[$location]['total_share_count_position'] : 'after');
			}
		}

		//add override styles
		global $ultimatesocialshare_inline_css;

		//create array if it doesn't exist yet
		if(!is_array($ultimatesocialshare_inline_css)) {
			$ultimatesocialshare_inline_css = array();
		}

		//make sure we haven't printed css for this location yet
		if(!in_array($location, $ultimatesocialshare_inline_css)) {

			//add used location to global flag
			$ultimatesocialshare_inline_css[] = $location;

			$styles = "";
			if(empty($ultimatesocialshare[$location]['labels'])) {
				$styles.= "body .ultimatesocialshare-buttons.ultimatesocialshare-" . $location . " .ultimatesocialshare-button-icon { width: 100%; }";
			}
			if(!empty($ultimatesocialshare[$location]['button_color'])) {
				$styles.= ".ultimatesocialshare-" . $location . " .ultimatesocialshare-button .ultimatesocialshare-button-block { background: " . $ultimatesocialshare[$location]['button_color'] . "; }";
				$styles.= ".ultimatesocialshare-" . $location . " .ultimatesocialshare-button .ultimatesocialshare-border { border-color: " . $ultimatesocialshare[$location]['button_color'] . "; }";
				$styles.= ".ultimatesocialshare-" . $location . " .ultimatesocialshare-button .ultimatesocialshare-inverse { color: " . $ultimatesocialshare[$location]['button_color'] . "; }";
			}
			if(!empty($ultimatesocialshare[$location]['border_radius']) && $ultimatesocialshare[$location]['shape']=='rounded') {
				$styles.= ".ultimatesocialshare-" . $location . " .ultimatesocialshare-button { border-radius: " . $ultimatesocialshare[$location]['border_radius'] . "px; }";
				$styles.= ".ultimatesocialshare-" . $location . " .ultimatesocialshare-button  { border-radius: " . $ultimatesocialshare[$location]['button_radius'] . "px; }";
				$styles.= ".ultimatesocialshare-" . $location . " .ultimatesocialshare-button{ border-radius: " . $ultimatesocialshare[$location]['button_radius'] . "px; }";
			}
			if(!empty($ultimatesocialshare[$location]['button_hover_color'])) {
				$styles.= ".ultimatesocialshare-" . $location . " .ultimatesocialshare-button:hover .ultimatesocialshare-button-block { background: " . $ultimatesocialshare[$location]['button_hover_color'] . "; }";
				$styles.= ".ultimatesocialshare-" . $location . " .ultimatesocialshare-button:hover .ultimatesocialshare-border { border-color: " . $ultimatesocialshare[$location]['button_hover_color'] . "; }";
				$styles.= ".ultimatesocialshare-" . $location . " .ultimatesocialshare-button:hover .ultimatesocialshare-inverse { color: " . $ultimatesocialshare[$location]['button_hover_color'] . "; }";
				$styles.= "body .ultimatesocialshare-" . $location . " a.ultimatesocialshare-button:hover .ultimatesocialshare-button-wrapper>span { box-shadow: none; filter: brightness(1); }"; //remove dynamic darkening
			}
			if(!empty($styles)) {
				$output.= "<style>" . $styles . "</style>";
			}
		}

		//buttons container
		$output.= "<div class='ultimatesocialshare-buttons ultimatesocialshare-" . $location . $container_class . " ultimatesocialshare-no-print'>";

			$output.= "<div class='ultimatesocialshare-buttons-wrapper'>";

				$output_buttons = "";

				//print buttons
				foreach($ultimatesocialshare[$location]['social_networks'] as $key => $network) {

					$class = $network;
					if(!empty($share_counts[$network]) && !empty($ultimatesocialshare[$location]['network_share_counts'])) {
						$class.= " ultimatesocialshare-share-count";
					}
					$target = '_blank';
					if(in_array($network, array('email', 'sms'))) {
						$target = '_self';
					}
					

					//button link
					$output_buttons.= "<a href='" . ultimatesocialshare_network_link($network) . "' aria-label='" . $networks[$network]['name'] . "' target='" . $target . "' class='ultimatesocialshare-button " . $class . "' rel='nofollow noopener noreferrer'" . ($network == 'pinterest' ? " data-pin-do='none'" : "") . ">";

						//button wrapper
						$output_buttons.= "<span class='ultimatesocialshare-button-wrapper ultimatesocialshare-button-block" . $button_class . "'>";

							//buton icon
							$output_buttons.= "<span class='ultimatesocialshare-button-icon ultimatesocialshare-button-block" . $icon_class . "'>";
								$output_buttons.= $networks[$network]['icon'];
								if(!empty($share_counts[$network])) {
									$output_buttons.= "<span class='ultimatesocialshare-button-share-count'>" . ultimatesocialshare_round_number($share_counts[$network]) . "</span>";
									$share_count = $share_count + $share_counts[$network];
								}
							$output_buttons.= "</span>";

							//button label
							if($location == 'inline') {
								$output_buttons.= "<span class='ultimatesocialshare-button-label ultimatesocialshare-button-block" . $label_class . (empty($ultimatesocialshare[$location]['labels']) ? " ultimatesocialshare-hide" : "") . "'>";
									$output_buttons.= "<span class='ultimatesocialshare-button-label-wrapper'>";
										$output_buttons.= $networks[$network]['name'];
									$output_buttons.= "</span>";
								$output_buttons.= "</span>";
							}

						$output_buttons.= "</span>";

					$output_buttons.= "</a>";
				}

				//show total share count
				if(!empty($ultimatesocialshare[$location]['total_share_count']) && !empty($share_count)) {
					$output_total_share_count = "";
					$output_total_share_count.= "<div class='ultimatesocialshare-total-share-count'>";
						$output_total_share_count.= "<div class='ultimatesocialshare-total-share-count-wrapper'>";
							$output_total_share_count.= "<div class='ultimatesocialshare-total-share-count-details'>";
									$output_total_share_count.= "<div class='ultimatesocialshare-total-share-count-amount'>" . ultimatesocialshare_round_number($share_count) . "</div>";
									$output_total_share_count.= "<div class='ultimatesocialshare-total-share-count-text'>" . ($share_count > 1 ? __('SHARES', 'ultimatesocialshare') : __('SHARE', 'ultimatesocialshare')) . "</div>";
							$output_total_share_count.= "</div>";
						$output_total_share_count.= "</div>";
					$output_total_share_count.= "</div>";

					if(!empty($ultimatesocialshare[$location]['total_share_count_position'])) {
						$output.= $output_total_share_count . $output_buttons;
					}
					else {
						$output.= $output_buttons . $output_total_share_count;
					}
				}
				else {
					$output.= $output_buttons;
				}

			$output.= "</div>";
		$output.= "</div>";
	}

	return $output;
}

//round number to nearest thousand, million, billion, trillion
function ultimatesocialshare_round_number($num) {

    if($num > 1000) {
        $x = round($num);
        $x_number_format = number_format($x);
        $x_array = explode(',', $x_number_format);
        $x_parts = array('k', 'm', 'b', 't');
        $x_count_parts = count($x_array) - 1;
        $x_display = $x;
        $x_display = $x_array[0] . ((int) $x_array[1][0] !== 0 && $x_array[0] < 99 ? '.' . $x_array[1][0] : '');
        $x_display .= $x_parts[$x_count_parts - 1];
        return $x_display;
    }

    return $num;
}



//check option update for custom button presses and process according function
function ultimatesocialshare_pre_update_option_ultimatesocialshare_button_triggers($new_value, $old_value) {

	//purge short links button was pressed
	if(!empty($new_value['purge_share_counts'])) {

		global $wpdb;

		$ultimatesocialshare_table = $wpdb->prefix . 'ultimatesocialshare_meta';

		//delete all share counts
		$result = $wpdb->query($wpdb->prepare("DELETE FROM $ultimatesocialshare_table WHERE meta_key = %s OR meta_key = %s OR meta_key = %s OR meta_key = %s", 'share_counts', 'share_counts_updated', 'recovery_share_counts', 'recovery_urls'));

		//display result message
		if($result !== false) {
			add_settings_error('ultimatesocialshare', 'ultimatesocialshare-purge-success', __('Share counts purged.', 'ultimatesocialshare'), 'updated');

		} else {
			add_settings_error('ultimatesocialshare', 'ultimatesocialshare-purge-error', __('Share counts not purged.', 'ultimatesocialshare'), 'error');
		}

		//prevent normal option update
		return $old_value;
	}

	//purge short links button was pressed
	if(!empty($new_value['purge_short_links'])) {

		global $wpdb;

		//delete all short links
		$result = $wpdb->delete($wpdb->prefix . 'ultimatesocialshare_meta', array('meta_key' => 'short_links'));

		//display result message
		if($result !== false) {
			add_settings_error('ultimatesocialshare', 'ultimatesocialshare-purge-success', __('Short links purged.', 'ultimatesocialshare'), 'success');

		} else {
			add_settings_error('ultimatesocialshare', 'ultimatesocialshare-purge-error', __('Short links not purged.', 'ultimatesocialshare'), 'error');
		}

		//prevent normal option update
		return $old_value;
	}

	return $new_value;
}




